#include<stdio.h>

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		c = c + 32;
	}
	return (c);
}

int	main(void)
{
	char	a;

	a = '?';
	printf("%c", ft_tolower(a));
}
